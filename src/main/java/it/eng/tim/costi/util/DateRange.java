package it.eng.tim.costi.util;

import java.util.Date;

public class DateRange {

	private Date startDate;
	private Date endDate;
	
	private String startNPATDate;
	private String endNPATDate;
	
	private String startPresentationDate;
	private String endPresentationDate;
	
	
	
	
	public Date getStartDate() {
		return startDate;
	}
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public String getStartNPATDate() {
		return startNPATDate;
	}
	public void setStartNPATDate(String startNPATDate) {
		this.startNPATDate = startNPATDate;
	}
	public String getEndNPATDate() {
		return endNPATDate;
	}
	public void setEndNPATDate(String endNPATDate) {
		this.endNPATDate = endNPATDate;
	}
	public String getStartPresentationDate() {
		return startPresentationDate;
	}
	public void setStartPresentationDate(String startPresentationDate) {
		this.startPresentationDate = startPresentationDate;
	}
	public String getEndPresentationDate() {
		return endPresentationDate;
	}
	public void setEndPresentationDate(String endPresentationDate) {
		this.endPresentationDate = endPresentationDate;
	}
	
	
	
	
	
	
	
}
